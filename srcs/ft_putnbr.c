#include "libft.h"

void	ft_putnbr(int n)
{
	int		ok;

	ok = 0;
	if (n == -2147483648)
	{
		ok = 1;
		ft_putstr("-2147483648");
	}
	if (ok == 0)
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = n * (-1);
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
			ft_putchar(48 + n);
	}
}
