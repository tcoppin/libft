#include "libft.h"

char	*ft_strcpy(char *dst, char const *src)
{
	int i;

	i = 0;
	if (src)
	{
		while (src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = src[i];
	}
	return (dst);
}
